namespace app\mobile\controller;
use think\Request;
use think\Db;
class Goods extends Base
{

    public function goodsinfo()
    {
    	$goodsid = input()['id'];
    	$res = Db::name('goods')->where('goods_id='.$goodsid)->find();
        $this->assign(['info' => $res]);
        return $this->fetch();
    }



}

